//Printing numbers from 1 to N

#include<iostream>
using namespace std;

int main()
{
        
        int N;
        cout<< "Enter the value of N"<< endl;
        cin>>N;
        cout<<"The sequence is : "<<endl;
        int i=1;
        while(i<=N)
        {
            cout<<i <<endl;
            i++;
        }

}
